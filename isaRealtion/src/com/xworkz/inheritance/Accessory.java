package com.xworkz.inheritance;

public class Accessory {
        public void wear() {
            System.out.println("Wearing an accessory...");
        }

        public void remove() {
            System.out.println("Removing the accessory...");
        }

        public void checkMaterial() {
            System.out.println("Checking the material of the accessory...");
        }

        public void clean() {
            System.out.println("Cleaning the accessory...");
        }

        public void store() {
            System.out.println("Storing the accessory safely...");
        }
    }

